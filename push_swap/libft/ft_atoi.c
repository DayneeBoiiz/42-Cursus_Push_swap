#include "libft.h"
#include <stdio.h>

int	ft_atoi(const char *str)
{
	size_t	i;
	long	nbr;
	int		symb;

	symb = 1;
	i = 0;
	nbr = 0;
	while (str[i] == 32 || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			symb = -1;
		i++;
	}
	while (str[i] && (str[i] >= '0') && (str[i] <= '9'))
	{
		nbr *= 10;
		nbr += str[i] - '0';
		i++;
	}
	if ((nbr * symb) > 2147483647 || (nbr * symb) < -2147483648)
		errno = ERANGE;
	return (nbr * symb);
}
